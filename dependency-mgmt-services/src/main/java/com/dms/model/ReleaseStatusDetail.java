package com.dms.model;

/**
 * @author Richa Prasad
 *
 */
public class ReleaseStatusDetail {

	private Long scope;
	private Long assessment;
	private Long funding;
	private Long design;
	private Long businessRequirement;
	private Long code;
	private Long test;
	/**
	 * @return the scope
	 */
	public Long getScope() {
		return scope;
	}
	/**
	 * @param scope the scope to set
	 */
	public void setScope(Long scope) {
		this.scope = scope;
	}
	/**
	 * @return the assessment
	 */
	public Long getAssessment() {
		return assessment;
	}
	/**
	 * @param assessment the assessment to set
	 */
	public void setAssessment(Long assessment) {
		this.assessment = assessment;
	}
	/**
	 * @return the funding
	 */
	public Long getFunding() {
		return funding;
	}
	/**
	 * @param funding the funding to set
	 */
	public void setFunding(Long funding) {
		this.funding = funding;
	}
	/**
	 * @return the design
	 */
	public Long getDesign() {
		return design;
	}
	/**
	 * @param design the design to set
	 */
	public void setDesign(Long design) {
		this.design = design;
	}
	/**
	 * @return the businessRequirement
	 */
	public Long getBusinessRequirement() {
		return businessRequirement;
	}
	/**
	 * @param businessRequirement the businessRequirement to set
	 */
	public void setBusinessRequirement(Long businessRequirement) {
		this.businessRequirement = businessRequirement;
	}
	/**
	 * @return the code
	 */
	public Long getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(Long code) {
		this.code = code;
	}
	/**
	 * @return the test
	 */
	public Long getTest() {
		return test;
	}
	/**
	 * @param test the test to set
	 */
	public void setTest(Long test) {
		this.test = test;
	}
}
