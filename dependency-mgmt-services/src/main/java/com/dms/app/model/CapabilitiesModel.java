package com.dms.app.model;

/**
 * @author Richa Prasad
 *
 */
public class CapabilitiesModel {
	private String capabilityId;
	private String capabilityDesc;
	/**
	 * @return the capabilityId
	 */
	public String getCapabilityId() {
		return capabilityId;
	}
	/**
	 * @param capabilityId the capabilityId to set
	 */
	public void setCapabilityId(String capabilityId) {
		this.capabilityId = capabilityId;
	}
	/**
	 * @return the capabilityDesc
	 */
	public String getCapabilityDesc() {
		return capabilityDesc;
	}
	/**
	 * @param capabilityDesc the capabilityDesc to set
	 */
	public void setCapabilityDesc(String capabilityDesc) {
		this.capabilityDesc = capabilityDesc;
	}
	

}
