package com.dms.app.model;

/**
 * @author Richa Prasad
 *
 */
public class CapabilityReportingModel {
	
	private String capabilityName;
	private String capabilityType;
	private String releaseDesc;
	private String projectDesc;
	private String applicationDesc;
	private String serviceDesc;
	private String capabilityOwner;
	/**
	 * @return the capabilityName
	 */
	public String getCapabilityName() {
		return capabilityName;
	}
	/**
	 * @param capabilityName the capabilityName to set
	 */
	public void setCapabilityName(String capabilityName) {
		this.capabilityName = capabilityName;
	}
	/**
	 * @return the capabilityType
	 */
	public String getCapabilityType() {
		return capabilityType;
	}
	/**
	 * @param capabilityType the capabilityType to set
	 */
	public void setCapabilityType(String capabilityType) {
		this.capabilityType = capabilityType;
	}
	/**
	 * @return the releaseDesc
	 */
	public String getReleaseDesc() {
		return releaseDesc;
	}
	/**
	 * @param releaseDesc the releaseDesc to set
	 */
	public void setReleaseDesc(String releaseDesc) {
		this.releaseDesc = releaseDesc;
	}
	/**
	 * @return the projectDesc
	 */
	public String getProjectDesc() {
		return projectDesc;
	}
	/**
	 * @param projectDesc the projectDesc to set
	 */
	public void setProjectDesc(String projectDesc) {
		this.projectDesc = projectDesc;
	}
	/**
	 * @return the applicationDesc
	 */
	public String getApplicationDesc() {
		return applicationDesc;
	}
	/**
	 * @param applicationDesc the applicationDesc to set
	 */
	public void setApplicationDesc(String applicationDesc) {
		this.applicationDesc = applicationDesc;
	}
	/**
	 * @return the serviceDesc
	 */
	public String getServiceDesc() {
		return serviceDesc;
	}
	/**
	 * @param serviceDesc the serviceDesc to set
	 */
	public void setServiceDesc(String serviceDesc) {
		this.serviceDesc = serviceDesc;
	}
	/**
	 * @return the capabilityOwner
	 */
	public String getCapabilityOwner() {
		return capabilityOwner;
	}
	/**
	 * @param capabilityOwner the capabilityOwner to set
	 */
	public void setCapabilityOwner(String capabilityOwner) {
		this.capabilityOwner = capabilityOwner;
	}
	

}
